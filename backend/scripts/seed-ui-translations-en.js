async function ensureLocale() {
  const existing = await Locale.findOne({ code: 'en' }).lean();
  if (existing) {
    if (!existing.isActive || existing.baseLocale) {
      await Locale.updateOne(
        { code: 'en' },
        { $set: { isActive: true, baseLocale: null, label: existing.label || 'English' } }
      );
    }
    return;
  }

  await Locale.create({ code: 'en', label: 'English', baseLocale: null, isActive: true });
}

async function run() {
  await connectDatabase();
  await ensureLocale();

  const operations = Object.entries(entries).map(([key, value]) => ({
    updateOne: {
      filter: { locale: 'en', key },
      update: { $set: { value } },
      upsert: true,
    },
  }));

  await Translation.bulkWrite(operations, { ordered: false });
  console.log(`Seeded ${operations.length} ui translation keys for en.`);
}

module.exports = {
  entries,
  ensureLocale,
  run,
};

if (require.main === module) {
  run()
    .catch((error) => {
      console.error('Failed to seed en UI translations:', error.message);
      process.exitCode = 1;
    })
    .finally(async () => {
      try {
        await mongoose.connection.close();
      } catch (error) {
        // Ignore close errors.
      }
    });
}
